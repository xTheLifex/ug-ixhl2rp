wg = wg or {}

/* --------------------------------- Configs -------------------------------- */

wg.OptionsCheck = function ()
    return !(LocalPlayer():IsAdmin())
end

wg.AddOption = function (name, type, default, data)
    if not name then return end
    if not type then return end
    local finaldata = {category="Wolf Guard", bNetworked = true, hidden = wg.OptionsCheck}
    if data then
        table.Merge(finaldata, data)
    end

    return ix.option.Add(name, type, default, finaldata)
end

wg.AddOption("wgPrintDeaths", ix.type.bool, true)
wg.AddOption("wgPrintDefaultCommands", ix.type.bool, false)
wg.AddOption("wgPrintCharacterEvents", ix.type.bool, true)
wg.AddOption("wgStaffPmUseRealName", ix.type.bool, false)
ix.option.Add("wgRecognizeBypass", ix.type.bool, false, { category = "Wolf Guard", hidden = wg.OptionsCheck}) -- Not networked.

/* --------------------------------- Access --------------------------------- */

CAMI.RegisterPrivilege({
	Name = "WolfGuard - Staff Chat",
	MinAccess = "admin"
})

/* ----------------------------- Console Logging ---------------------------- */

function PLUGIN:Log(text)
    print("[WolfGuard] " .. text)
end

function PLUGIN:Warn(text)
    print("[WolfGuard] (WARNING!): " .. text)
end

function PLUGIN:Error(text, halt)
    local halt = halt or false
    if (halt) then
        error("[WolfGuard] " .. text)
    else
        ErrorNoHalt("[WolfGuard] " .. text)
    end
end

